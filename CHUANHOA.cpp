#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n;
    cin >> n;
    cin.ignore();
    while (n--)
    {
        string s;
        getline(cin, s);
        for (int i = 0; i < s.size(); i++)
        {
            if (s[0] == ' ')
            {
                s.erase(0, 1);
            }
            if (s[i] == ' ' && s[i + 1] == ' ')
            {
                s.erase(i, 1);
                i--;
            }
            if (s[s.size() - 1] == ' ')
            {
                s.erase(s.size() - 1, 1);
            }
        }
        cout << s << endl;
    }
}