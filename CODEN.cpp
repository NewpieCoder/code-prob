#include <bits/stdc++.h>
using namespace std;
int main()
{
    long long m, n;
    cin >> m >> n;
    if (m % 2 == 0 && n % 2 == 0)
    {
        cout << m * n / 4;
    }
    if (m % 2 == 0 && n % 2 != 0)
    {
        cout << (n - 1) * m / 4 + m / 2;
    }
    if (n % 2 == 0 && m % 2 != 0)
    {
        cout << (m - 1) * n / 4 + (n) / 2;
    }
    if (m % 2 != 0 && n % 2 != 0)
    {
        cout << (m - 1) * (n - 1) / 4 + (m + n) / 2;
    }
}